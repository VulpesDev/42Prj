#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	unsigned char	*p = s;

	i = 0;
	while (*p && i < n)
	{
		p[i] = c;
		i++;
	}
	return (s);
}
